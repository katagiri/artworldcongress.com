    <h2>A few words from the Conference Organizers</h2>
    <p>The Annual Meeting on safe and effective approaches for assisted reproductive technology in Shanghai has gained great widespread success and popularity.  The 2014 meeting will mark the 5th year of this event and we are proud to invite you all to join us in Shanghai to participate. We expect that you will find it to be an excellent scientific program and a diverse social experience.</p>
    <p>In the 2014 meeting, while we will continue the address topics like the mechanism of ovulation and continue to search for the proper, efficient ways of controlling premature ovulation, we will also expand our discussions in new directions, taking a look at things like the physiology of human embryo implantation and the genetic screening of pre-implantation embryos.</p>
    <p>Finally, we will be adding a new evening session to the program &ndash; a unique interactive lecture. The interactive lecture will give the audience a chance to participate in our discussion. For those we are interested in participating in this interactive session, we encourage you to submit a difficult clinic case that you come across in your practice when you submit your registration form.</p>
